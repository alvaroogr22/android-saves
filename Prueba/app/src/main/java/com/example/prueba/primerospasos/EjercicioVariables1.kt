package com.example.prueba.primerospasos

fun main(){
    //definicion de variables
    var valor1: Int
    var valor2: Int
    var resultado: Int
    //asignacion de valores
    valor1 = 100
    valor2 = 400
    resultado = valor1 + valor2
    //imprimir por pantalla
    println("La suma de $valor1 + $valor2 es: $resultado")

    valor1 = 570
    valor2 = 553
    resultado = valor1 - valor2
    //imprimir por pantalla
    println("La resta de $valor1 - $valor2 es: $resultado")

    valor1 = 2
    valor2 = 3
    resultado = valor1 * valor2
    //imprimir por pantalla
    println("La multiplicación de $valor1 * $valor2 es: $resultado")

    valor1 = 100
    valor2 = 4
    resultado = valor1 / valor2
    //imprimir por pantalla
    println("La división de $valor1 / $valor2 es: $resultado")
}